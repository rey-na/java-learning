package java_data_types;

public class Day07_Loop {

	public static void main(String[] args) {
		System.out.println("before the loop");
		for(int i = 0; i<=10;i++) {
			System.out.println("hello world");
			
		}System.out.println("after the loop");
		for(int i =10; i>= 0; i--) {
			System.out.println(i);
		}

	}

}
